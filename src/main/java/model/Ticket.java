package model;

public class Ticket {
    private Long id;
    private String name;
    private Long price;

    public Ticket() {
        this.name = "";
        this.price = 0L;
    }

    public Ticket(Long id, String name, Long price) {
        this.id = id;
        this.name = name;
        this.price = price;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getPrice() {
        return price;
    }

    public void setPrice(Long price) {
        this.price = price;
    }

    @Override
    public String toString() {
        return name + " (Value: " + price + ")";
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }

        if (obj == null || getClass() != obj.getClass()) {
            return false;
        }

        Ticket something = (Ticket) obj;
        return this.getId().equals(something.getId());
    }
}
